export default function EmojiPicker({ value, onChange, placeholder = '😊' }: EmojiPickerProps) {
  const [isOpen, setIsOpen] = useState(false)
  const [activeCategory, setActiveCategory] = useState<string>(Object.keys(EMOJI_CATEGORIES)[0])

  return (
    <div className="relative">
      {/* Emoji Display Button */}
      <div className="relative">
        <button
          type="button"
          onClick={() => setIsOpen(!isOpen)}
          className="w-full p-3 border border-slate-200 rounded-xl focus:outline-none focus:ring-2 focus:ring-blue-500 focus:border-transparent text-center text-2xl hover:bg-slate-50 transition-colors"
        >
          {value || <span className="opacity-40">{placeholder}</span>}
        </button>
        
        {/* Clear button overlay */}
        {value && (
          <button
            type="button"
            onClick={(e) => {
              e.stopPropagation()
              onChange('')
            }}
            className="absolute top-0 right-0 p-1 text-slate-400 hover:text-slate-600 transition-colors"
            title="Clear emoji"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
            </svg>
          </button>
        )}
      </div>

      {/* Emoji Picker Dropdown */}
      {isOpen && (
        <>
          {/* Backdrop */}
          <div 
            className="fixed inset-0 z-40" 
            onClick={() => setIsOpen(false)}
          />
          
          {/* Picker */}
          <div className="absolute top-full mt-2 w-80 bg-white rounded-xl shadow-2xl border border-slate-200 z-50 overflow-hidden">
            {/* Category Tabs */}
            <div className="flex border-b border-slate-200 overflow-x-auto">
              {Object.keys(EMOJI_CATEGORIES).map((category) => (
                <button
                  key={category}
                  type="button"
                  onClick={() => setActiveCategory(category)}
                  className={`px-3 py-2 text-sm font-medium whitespace-nowrap transition-colors ${
                    activeCategory === category
                      ? 'text-blue-600 border-b-2 border-blue-600 bg-blue-50'
                      : 'text-slate-600 hover:text-slate-900 hover:bg-slate-50'
                  }`}
                >
                  {category}
                </button>
              ))}
            </div>

            {/* Emoji Grid */}
            <div className="max-h-60 overflow-y-auto p-2">
              <div className="grid grid-cols-8 gap-1">
                {EMOJI_CATEGORIES[activeCategory as keyof typeof EMOJI_CATEGORIES].map((emoji) => (
                  <button
                    key={emoji}
                    type="button"
                    onClick={() => {
                      onChange(emoji)
                      setIsOpen(false)
                    }}
                    className="p-2 text-xl hover:bg-slate-100 rounded transition-colors"
                  >
                    {emoji}
                  </button>
                ))}
              </div>
            </div>

            {/* Clear Button */}
            {value && (
              <div className="border-t border-slate-200 p-2">
                <button
                  type="button"
                  onClick={() => {
                    onChange('')
                    setIsOpen(false)
                  }}
                  className="w-full text-sm text-slate-600 hover:text-slate-900 py-2 hover:bg-slate-50 rounded transition-colors"
                >
                  Clear emoji
                </button>
              </div>
            )}
          </div>
        </>
      )}
    </div>
  )
}